function add(a,b){
  return a + b;
}
function subtract(a,b){
  return a - b;
}
function multiply(a,b){
  return a * b;
}
function divide(a,b){
  return a / b;
}
function makeInt(num){
  return parseInt(num, 10);
}
function preserveDecimal(num){
  return parseFloat(num);
}
function increment(num){return ++num;}
function decrement(num){return --num;}